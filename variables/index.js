// variables are containers for storing values
// variables can be declared using: let, var, const keywords
var x = 5;
var y = 20;
var z = x + y;

console.log(z);
//let
let x1 = 50;
let y1 = 20;
let z1 = x1 + y1;

console.log(z1);
//const
const x2 = 40;
const y2 = 20;
const Y2 = 40; // Different from y2(case sensitive)
const z2 = x2 + Y2;

console.log(z2);
